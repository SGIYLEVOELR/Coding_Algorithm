package A_B_사칙연산;

import java.util.Scanner;

public class Main_10869 {

	public static void main(String[] args) {
		
		Scanner scan = new Scanner(System.in);
		
		int A = scan.nextInt();
		int B = scan.nextInt();
		
		System.out.println("A + B = " + (A + B));
		System.out.println("A - B = " + (A - B));
		System.out.println("A X B = " + (A * B));
		System.out.println("A / B = " + (A / B));
		System.out.println("A % B = " + (A % B));
	}
}
